CREATE TABLE branches (branch_code VARCHAR(10) PRIMARY KEY, branch_name VARCHAR(50), course_name VARCHAR(10));
CREATE TABLE students (enrolment_number INT PRIMARY KEY, student_name VARCHAR(40), fathers_name VARCHAR(40), mothers_name VARCHAR(40), roll_number INT, semester_number INT, spi_1 DOUBLE(3, 2), spi_2 DOUBLE(3, 2), spi_3 DOUBLE(3, 2), spi_4 DOUBLE(3, 2), spi_5 DOUBLE(3, 2), branch_code VARCHAR(10), FOREIGN KEY (branch_code) REFERENCES branches(branch_code) ON DELETE SET NULL);
CREATE TABLE subjects (subject_code VARCHAR(10), branch_code VARCHAR(10), subject_name VARCHAR(50), credits INT, semester_number INT, PRIMARY KEY (subject_code, branch_code), FOREIGN KEY (branch_code) REFERENCES branches(branch_code) ON DELETE CASCADE);
CREATE TABLE grade_points (enrolment_number INT, subject_code VARCHAR(10), grade_point INT, PRIMARY KEY (enrolment_number, subject_code), FOREIGN KEY(enrolment_number) REFERENCES students(enrolment_number) ON DELETE CASCADE, FOREIGN KEY(subject_code) REFERENCES subjects(subject_code) ON DELETE CASCADE);

